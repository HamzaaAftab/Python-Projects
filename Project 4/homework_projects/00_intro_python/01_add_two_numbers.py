def main():
    # Input First Number
    num1 = int(input("Enter First Number: "))
    
    #Input Second Number
    num2 = int(input("Enter Second Number: "))
    
    # Calculate Sum
    sum = num1 + num2
    
    # Print the Sum
    print(f"The Sum of {num1} and {num2} is {sum}")
    
    
if __name__ == '__main__':
    main()